# 2. Write a program that inputs temperature in Celsius and converts it to Fahrenheit. The formula for conversion is
# Temperature in Fahrenheit = Temperature in Celsius * 1.8 + 32

temp_in_celsius = float(input("Enter the temperature in celsius"))
temp_in_farenheit = temp_in_celsius*1.8 + 32
print(f'The entered temperature {temp_in_celsius} celsius is equal to {temp_in_farenheit} farenheit')

